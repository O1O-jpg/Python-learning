# 单引号定义
name_A = 'Alan'
# 双引号定义
name_B = "Bob"
# 三引号定义
name_C = """Clark"""

print("A's name:",name_A)
print("B's name:",name_B)
print("C's name:",name_C)

# 引号嵌套
# 内含双引号
name_A = '"Alex"'
# 内含单引号
name_B = "'Blanc'"
# 转移字符
name_C = """\"Chris\""""


print("A's name:",name_A)
print("B's name:",name_B)
print("C's name:",name_C)
